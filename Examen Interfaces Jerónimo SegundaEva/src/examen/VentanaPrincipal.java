    private void configurarRendererTemperaturas() {
    tabla.setDefaultRenderer(Object.class, new DefaultTableCellRenderer() {
        @Override
        public Component getTableCellRendererComponent(JTable tabla, 
                Object value, boolean isSelected, boolean hasFocus, 
                int row, int column) {


            Component c = super.getTableCellRendererComponent(tabla, value, 
                    isSelected, hasFocus, row, column);


            if (!isSelected) {
                if (column == 2) {

                    String tempStr = tabla.getValueAt(row, 2).toString();
                    double temp = Double.parseDouble(tempStr);


                    if (temp > 30) {
                        c.setBackground(Color.RED);
                    } else if (temp >= 20) {

                        c.setBackground(Color.ORANGE);
                    } else {

                        c.setBackground(Color.BLUE);
                    }
                    setForeground(Color.WHITE); 
                } else {

                    c.setBackground(Color.WHITE);
                    c.setForeground(Color.BLACK);
                }
            } else {

                c.setBackground(tabla.getSelectionBackground());
                c.setForeground(tabla.getSelectionForeground());
            }

            return c;
        }
    });
}
    
