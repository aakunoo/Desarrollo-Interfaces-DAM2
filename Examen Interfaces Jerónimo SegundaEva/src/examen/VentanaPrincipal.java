    private void menuItemEliminarActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_menuItemEliminarActionPerformed
        int respuesta = JOptionPane.showConfirmDialog(this, java.util.ResourceBundle.getBundle("examen/VentanaPrincipal").getString("DESEAS ELIMINAR ESTOS CAMPOS?"), java.util.ResourceBundle.getBundle("examen/VentanaPrincipal").getString("DESEAS ELIMINAR LOS CAMPOS SELECCIONADOS?"), JOptionPane.YES_NO_OPTION);
        
        if(respuesta == JOptionPane.YES_OPTION){
            int [] filas = tabla.getSelectedRows();
        if(filas.length>0){
            for(int i = filas.length-1; i>=0; i--){
            ((DefaultTableModel)tabla.getModel()).removeRow(filas[i]);
            }
        }
        }
    }//GEN-LAST:event_menuItemEliminarActionPerformed

