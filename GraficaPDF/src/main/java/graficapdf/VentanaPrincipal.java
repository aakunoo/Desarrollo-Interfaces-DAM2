    private void menuItemArchivoActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_menuItemArchivoActionPerformed
        if(fileChooser.showSaveDialog(this) == JFileChooser.APPROVE_OPTION){
            try {
                File fichero = fileChooser.getSelectedFile();
              //String nombreFichero =  fichero.getName();
             //if(nombreFichero.substring(nombreFichero.length()-4).equalsIgnoreCase(".pdf")); // para coger la extensi√≥n con el .
             
                if(!fichero
                        .getName()
                        .toLowerCase()
                        .endsWith(".pdf")) {
                    
                    fichero = new File(fichero.getAbsoluteFile()+ ".pdf");
                }

               Document documento = new Document(new PdfDocument(new PdfWriter(fichero)));
               
               documento.add(new Paragraph("Hola Mundo!"));
               List lista = new List();
             
               lista.add("Peras");
               lista.add("Manzanas");
               lista.add("Naranjas");
               
               ListItem naranjas = new ListItem("naranjas");
              //naranjas.setFontColor(Color.createColorWithColorSpace(new float[] {255f, 150f, 0f} ));
               lista.add(naranjas);
               documento.add(lista);
               documento.close();
               
            } catch (FileNotFoundException ex) {
                Logger.getLogger(VentanaPrincipal.class.getName()).log(Level.SEVERE, null, ex);
            }
        }
    }//GEN-LAST:event_menuItemArchivoActionPerformed

