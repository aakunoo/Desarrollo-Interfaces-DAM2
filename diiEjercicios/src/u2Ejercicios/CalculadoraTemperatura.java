    private void jButton1ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButton1ActionPerformed
      // Método que se ejecuta al pulsar el botón "Calcular"
        try {
            // Obtener el valor de la temperatura introducida
            double valor = Double.parseDouble(jTextField2.getText());

            // Obtener las unidades seleccionadas del combobox
            String de = jComboBox1.getSelectedItem().toString();
            String a = jComboBox2.getSelectedItem().toString();

            // Comprobar que las unidades no estén vacías
            if (!de.equals(" ") && !a.equals(" ")) {
                // Realizar la conversión
                double resultado = convertirTemperatura(valor, de, a);

                // Mostrar el resultado en el campo de texto de resultado
                jTextField3.setText(String.format("%.2f", resultado));
            } else {
                // Si alguna de las unidades no está seleccionada
                jTextField3.setText("Selecciona unidades");
            }
        } catch (NumberFormatException e) {
            // Manejar el error si el valor introducido no es un número válido
            jTextField3.setText("Introduce un número válido");
        }
   
    }//GEN-LAST:event_jButton1ActionPerformed

