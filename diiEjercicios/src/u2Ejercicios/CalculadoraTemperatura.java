    // Método para convertir temperaturas usando if-else
    private double convertirTemperatura(double valor, String de, String a) {
        double valorConvertido = 0;

        // Convertir de cualquier unidad a grados Celsius primero
        double valorEnCelsius = 0;
        if (de.equals("ºC")) {
            valorEnCelsius = valor;
        } else if (de.equals("ºF")) {
            valorEnCelsius = (valor - 32) * 5 / 9;
        } else if (de.equals("K")) {
            valorEnCelsius = valor - 273.15;
        }

        // Ahora convertir desde Celsius a la unidad deseada
        if (a.equals("ºC")) {
            valorConvertido = valorEnCelsius;
        } else if (a.equals("ºF")) {
            valorConvertido = valorEnCelsius * 9 / 5 + 32;
        } else if (a.equals("K")) {
            valorConvertido = valorEnCelsius + 273.15;
        }

        return valorConvertido;
    }
    
