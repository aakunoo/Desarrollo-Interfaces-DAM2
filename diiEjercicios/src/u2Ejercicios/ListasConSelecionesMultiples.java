    private void jbConfActionPermorfed(javax.swing.event.ListSelectionEvent evt) {//GEN-FIRST:event_jbConfActionPermorfed
               lbTexto.setText("<html>");
        
        if (lista.getSelectedValuesList().isEmpty()) {
            lbTexto.setForeground(Color.red);
            lbTexto.setText("Tienes que seleccionar alguna opción magick");
            return;
        }
        
        lbTexto.setForeground(Color.BLACK);
        lbTexto.setText(lbTexto.getText()+"Los valores introducidos son: " + lista.getSelectedValuesList().size());
        lbTexto.setText(lbTexto.getText() + "<ol>");

        for (int i = 0; i < lista.getSelectedValuesList().size(); i++) {
            lbTexto.setText(lbTexto.getText() + "<li>" + lista.getSelectedValuesList().get(i) + "</li>");
        }

        lbTexto.setText(lbTexto.getText() + "</ol></html>");
    }//GEN-LAST:event_jbConfActionPermorfed

