    private void btnAnnadirActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnAnnadirActionPerformed

        String nombre = txfNombre.getText();
        Integer puntos = (Integer) spnPuntos.getValue();
        
        if(nombre.isBlank()) {
            BorderUIResource bordeRojo = new BorderUIResource(BorderFactory.createLineBorder(Color.RED, 3));
            txfNombre.setBorder(bordeRojo);
            JOptionPane.showMessageDialog(this, "Faltan datos", "No se incluyó el nombre", JOptionPane.WARNING_MESSAGE);
        } else {
            SwingUtilities.updateComponentTreeUI(txfNombre);
            ((DefaultTableModel)tabla.getModel()).addRow(new Object[] {nombre, puntos});            
        }
        
        
    }//GEN-LAST:event_btnAnnadirActionPerformed

    private void btnEliminarActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnEliminarActionPerformed
        int[] filasSeleccionadas = tabla.getSelectedRows();
        
        if (filasSeleccionadas.length > 0) {
            if (JOptionPane.showConfirmDialog(rootPane, "¿Está seguro de que desea eliminar las filas seleccionadas", "Confirmación", JOptionPane.YES_NO_OPTION) == JOptionPane.YES_OPTION) {
                for(int i = filasSeleccionadas.length-1; i >= 0; i--) {
                    ((DefaultTableModel) tabla.getModel()).removeRow(filasSeleccionadas[i]);
                } 
            }
        } else {
            JOptionPane.showMessageDialog(this, "Debe seleccionar al menos una fila en la tabla ", "Ningún elemento seleccionado", JOptionPane.WARNING_MESSAGE);
        }
    }//GEN-LAST:event_btnEliminarActionPerformed

    private void btnVerActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnVerActionPerformed
        int[] filasSeleccionadas = tabla.getSelectedRows();
        
        String mensaje;
        if(filasSeleccionadas.length > 0) {
            mensaje = "<html>Los equipos seleccionados son:<ul>";
                    
            for(int i = 0; i < filasSeleccionadas.length; i++) {
                mensaje += "<li>" + (String) ((DefaultTableModel) tabla.getModel()).getValueAt(filasSeleccionadas[i], 0) + "</li>";
            }
            
            mensaje += "</ul></html>";
        } else {
            mensaje = "No ha seleccionado ningún elemento de la tabla";
        }   
                
        JOptionPane.showMessageDialog(this,  mensaje, "Elementos seleccionados", JOptionPane.INFORMATION_MESSAGE);
    }//GEN-LAST:event_btnVerActionPerformed

